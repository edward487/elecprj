                <h3><strong>MICROPROCESSOR SYSTEM</strong></h3>
            </div>
            <div class="col-md-12">
                <p class="ridge">The study of the design, and application of microprocessor systems based on the stated requirements. The focus is on the basic understanding of its structure and function to appreciate the architectural design of the microprocessor. The students are encouraged to study various types of microprocessors to acquire a better understanding of microprocessors.
                <br><br>
                Prerequisite: Computer System Organization w/ Assembly Language; Advanced Logic Circuits
                </p>
            </div><br><br><br><br><br><br><br><br><br><br> 
            <div class="col-md-12">
                <h3><strong>SAFETY ENGINEERING</strong></h3>
            </div>
            <div class="col-md-12">
                <p class="ridge">Evolution of safety management; safety terminology; safety programs adapted by high risk industries; hazards in the construction, manufacturing gas and power plants, and other engineering industries and how to prevent or irrigate them; techniques in hazard identification and analysis in workplaces; off-the-job safety; disaster prevention and mitigation; and incident investigation.
                <Br><Br>
                Prerequisite: At least 4th year standing
                </p>
            </div><br><br><br><br><br><br><br><br><br><br> 
            <div class="col-md-12">
                <h3><strong>DYNAMICS OF RIGID BODIES</strong></h3>
            </div>
            <div class="col-md-12">
                <p class="ridge">Kinetics and kinematics of particle; kinetics and kinematics of rigid bodies; work energy method; and impulse and momentum.
                <br><br>
                Prerequisite: Statics of Rigid Bodies
                </p>
            </div><br><br><br><br><br><br><br><br><br><br> 
            <div class="col-md-12">
                <h3><strong>STRENGTH OF MATERIALS</strong></h3>
            </div>
            <div class="col-md-12">
                <p class="ridge">Axial stress and strain; stress for torsion and bending; combined stresses; beam deflections; indeterminate beams; and elastic instability.
                <br><br>
                Prerequisite: Statics of Rigid Bodies
                </p>
            </div><br><br><br><br><br><br><br><br><br><br> 
            <div class="col-md-12">
                <h3><strong>CONTROL SYSTEMS</strong></h3>
            </div>
            <div class="col-md-12">
                <p class="ridge">Includes the control; devises equations of a system and block diagram systems.
                <Br><br>
                Prerequisite: Electric Circuits 2; Electronic Devices and Circuits
                </p>
