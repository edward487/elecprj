            </div>
            <div class="col-md-12">
                <p class="ridge">Introduces object-oriented programming concepts and techniques using an object-oriented programming language (e.g. C++, Java, Python, etc.). It covers the imperative language features of the language in comparison with C and involves the students in graphical user interface (GUI) development. The course also involves the students in hands-on work using various software tolls based upon the latest Software Development Kit (SDK) of the programming language used.
                <br><br>
                Prerequisite: Data Structure and Algorithm Analysis
                </p>
            </div><br><br><br><br><br><br><br><br><br><br> 
