                <div class="col-sm-12">
                    <p class="ridge">
                        1. Registration of student shall only be on the regular registration period indicated in the academic calendar. No student shall be registered in any subject after 12% of regular class meeting had been held.
                    </p>
                    <p class="ridge">
                        2. A student is considered officially enrolled when he has paid his tuition and other fees duly validated by the Assessment and Billing Section.
                    </p>
                    <p class="ridge">
                        3. Student must be officially registered in order to receive credit for course work.
                    </p>
                    <p class="ridge">
                        4. Admission slip/ Clearance / Class cards showing the scholastic standing of an old student shall be required as credentials for enrollment.
                    </p>
                    <p class="ridge">
                        5. A student will not be allowed to enroll in more than one course during his stay in the College.
                    </p>
                    <p class="ridge">
                        6. Rules in sequencing of subjects (prerequisite) in a curriculum should be observed and followed.
                    </p>
                    <p class="ridge">
                        7. Registration of a returning student is dependent on his previous scholastic records.
                    </p>
                    <p class="ridge">
                        8. Special students shall be allowed to enroll six units per semester and twelve units a year, for a period of only one year.
                    </p>
                    <p class="ridge">
                        9. Late registration fee shall be charged to as student who shall enroll after the regular registration period as provided for in the academic calendar.
                    </p>
                    <br><br><br><br>
                    <h4><strong>1.	Academic Load</strong></h4>
                    <p class="ridge">
                        a.	The total number of units in regular semester for which a student may register shall be in accordance with the curriculum program the student is enrolled in.
                    </p>
                    <p class="ridge">
                        b.	During Summer Term, a student may register only nine units, except for graduating students who shall be allowed to a higher load not exceeding twelve units.
                    </p>
                    <p class="ridge">
                        c.	Only graduating students shall be allowed to overload subject to their academic standing. Maximum of six units overload shall be allowed subject to the endorsement of the Dean and approval of the Vice President for Academic Affairs.
                    </p>
                    <p class="ridge">
                        d.	Students who are scholastic delinquent shall not be allowed to have an overload.
                    </p>
                    <p class="ridge">
                        e.	Load of transfer students shall be limited only to those subjects without prerequisite until such time his/her subjects have already been validated/accredited.
                    </p>
                    <p class="ridge">
                        f.	The maximum study load of the regular students in the master’s and doctoral program shall not exceed nine academic units per semester and six academic units per summer term.
                    </p>
                    <p class="ridge">
                        g.	The dean may limit the academic load of students who are employed outside the institution whether full time or part time.
                    </p>
                    <br><br>
                    <h4><strong>2.	Cross-enrollment</strong></h4>
                    <p class="ridge">
                        a.	Cross-enrollment should be done within the period of registration.
                    </p>
                    <p class="ridge">
                        b.	Cross-enrollment shall be allowed only for graduating students for a maximum of six units, unless otherwise those are the last number of units to be taken to qualify for graduation, and provide that these subjects are not offered in the institution, must have the same course description and number of units, and must not be a major subject.
                    </p>
                    <p class="ridge">
                        c.	No student shall be allowed to cross-register in two or more schools outside the College.
                    </p>
                    <p class="ridge">
                        d.	Cross-enrollment of students outside the College must have the approval of the Dean and VP for Academic Affairs and authorized by the College Registrar for the subject/s to be given credit by the College.
                    </p>
                    <p class="ridge">
                        e.	Students coming from other institution shall be allowed to cross-register within the enrollment period only and with a permit to cross-enroll from his Registrar and subject to the availability of a slot.
                    </p>
                    <br><br>
                    <h4><strong>3.	Changing/Adding/Dropping of Subjects</strong></h4>
                    <p class="ridge">
                        a.	Changing/Adding/Dropping of subjects shall be made only for valid reasons and stated in writing to be approved by the Dean and acknowledged by the College Registrar.
                    </p>
                    <p class="ridge">
                        b.	Changing/Adding/Dropping of subjects should be made within the specified period of time and subject to payment of corresponding fees.
                    </p>
                    <p class="ridge">
                        c.	Changing a subject shall be made only for valid reasons and when schedules allow the same.
                    </p>
                    <p class="ridge">
                        d.	Changed/Added subjects unofficially or without prior approval of the Dean shall not be given credits.
                    </p>
                    <p class="ridge">
                        e.	Total load carried by a student including the added subject/s should not exceed the maximum requirement under the rule of the academic load or that is prescribed for his curriculum year during the term/semester.
                    </p>
                    <p class="ridge">
                        f.	Dropping a subject shall be allowed before midterm. The midterm, a student may be allowed to drop subject for a valid and justifiable reason.
                    </p>
                    <p class="ridge">
                        g.	Unofficial dropping of subject after the mid-term shall earn the student a failing grade of 5.0.
                    </p>
                    <br><br>
                    <h4><strong>4.	Shifting from One Course to Another</strong></h4>
                    <p class="ridge">
                        a.	A student shall be allowed by the Dean to shift his course if in the opinion of the Dean, he is not academically suited to the course.
                    </p>
                    <p class="ridge">
                        b.	A student should apply for a shift of course to the Dean where he is getting out to be endorsed to the Dean of his new course who in turn shall approve said application.
                    </p>
                    <br><br>
                    <h4><strong>5.	Withdrawal of Registration</strong></h4>
                    <p class="ridge">
                        a.	Withdrawal of registration shall be made and approved based on existing rules and regulations of the institutions.
                    </p>
                    <p class="ridge">
                        b.	A student who withdraws his registration shall be entitled to full refund of fees paid.
                    </p>
                    <p class="ridge">
                        c.	A student who withdraws his registrations shall be entitled to withdraw his credentials submitted as requirement for enrollment.
                    </p>
                    <p class="ridge">
                        d.	No withdrawal of registration shall be made after the specified enrollment period. Rules of dropping shall be applied.
                    </p>
