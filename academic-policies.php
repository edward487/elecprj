                </div>
                <div class="col-sm-12">
                    <p class="ridge">
                        a. Computation of the general average of students with honors specially Summa Cum Laude, Magna Cum Laude, and Cum Laude shall be based on the following grade requirements:
		                  <br>&emsp;&emsp;&emsp;&emsp;&emsp;Summa Cum Laude – 1.20 to 1.00
		                  <br>&emsp;&emsp;&emsp;&emsp;&emsp;Magna Cum Laude – 1.45 to 1.21
		                  <br>&emsp;&emsp;&emsp;&emsp;&emsp;Cum Laude – 1.75 to 1.46
                    </p>
                    <p class="ridge">
                        b. In the computation of grades of candidates for honors rounding off of final grades shall not be allowed.
                    </p>
                    <p class="ridge">
                        c. Only final grades shall be considered in the computation of the general average.
                    </p>
                    <p class="ridge">
                        d. In computation of the final average of students who are candidates of graduation with honors, only resident credits shall be included.
                    </p>
                    <p class="ridge">
                        e. Candidates for graduation with honors should not have any grade lower than 2.50 (cum laude), 2.0 (magna and summa cum laude) and not removed “incomplete” in any academic or non-academic subject, whether prescribed or not in his/her curriculum, taken in the University or in another educational institution.
                    </p>
                    <p class="ridge">
                        f. Students who are candidates for graduation with honors must not have repeated a subject in another educational institution.
                    </p>
                    <p class="ridge">   
                        g. Students who are candidates for graduation with honors must have completed in the University at least 75% of the total academic units for graduation and must have been in residence therein for at least 2 years immediately prior to graduation.
                    </p>
                    <p class="ridge">
                        h. Student who are candidates for graduation with honors must have taken during each semester not less than 15 units of credits or the normal/regular load prescribed in his curriculum.
                    </p>
                    <p class="ridge">
                        i. A candidate for graduation who meet the prescribed grade average but failed to satisfy other requirements for honors shall be awarded the “Certificate of Graduation with Academic Distinction.”
                    </p>
                    <p class="ridge">
                        j. Students who are candidates for graduation with honors in courses with prescribed length of less than four years, the English Equivalent, “With Highest Honors”, “With High Honors”, “With Honors” shall be used.
                    </p>
                    <p class="ridge">
                        k. Students who are candidate for graduation with honors must not have been charged and found guilty of any violation of existing College rules which is at least one week suspension.
                    </p>
